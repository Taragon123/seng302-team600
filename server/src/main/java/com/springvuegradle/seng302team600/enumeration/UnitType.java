package com.springvuegradle.seng302team600.enumeration;

/**
 * An enum to describe different data types used by Unit instances.
 * Possible unit types include:
 * TEXT - String data type
 * NUMBER - Integer data type
 * DATE - Date data type (ISO 8601)
 * DATE_AND_TIME - Data and Time data type (ISO 8601)
 * TIME - Time data type (ISO 8601)
 * BOOLEAN - True or False data type
 */
public enum UnitType {

    TEXT,
    NUMBER,
    DATE,
    DATE_AND_TIME,
    TIME,
    BOOLEAN

}
